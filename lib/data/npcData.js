export const getNPCById = (id) => {
  return npcData.find(npc => npc.id === id);
};

export const getNPCsByStoryline = (storyline) => {
  return npcData.filter(npc => npc.storyline === storyline).sort((a, b) => a.order - b.order);
};

export const getNPCsByExpansion = (expansion) => {
  return npcData.filter(npc => npc.expansion === expansion).sort((a, b) => a.order - b.order);
};

export const getNPCsByStatus = (status) => {
  return npcData.filter(npc => npc.status === status).sort((a, b) => a.order - b.order);
}; 